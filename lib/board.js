Board.prototype.draw = function(piece) {
    var y = piece.shape.length;
    var x = piece.shape[0].length;

// TODO: Collision detection: only overwrite setBoard valuse if they are 0
    // interpolate each value into the respective spot on the setBoard, based on topLeft position
    for (var i = 0; i < y; i ++) {
      for (var j = 0; j < x; j++ ) {
        this.setBoard[piece.topLeftOrigin.row + i][piece.topLeftOrigin.col + j] = piece.shape[i][j];
      }
    }    

};

