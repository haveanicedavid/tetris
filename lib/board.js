Board.prototype.draw = function(piece) {
    var y = piece.shape.length; // 2
    var x = piece.shape[0].length; // 2

    for (var i = 0; i < y; i ++) {
      for (var j = 0; j < x; j++ ) {
        this.setBoard[piece.topLeft.row + i][piece.topLeft.col + j] = piece.shape[i][j];
      }
    }    

};

